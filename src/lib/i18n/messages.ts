export const messages: Record<Locale, typeof ru> = { ru, uz, en };

function resolvePath(dict: Record<string, unknown>, path: string): string | undefined {
  const parts = path.split(".");
  let cur: unknown = dict;
  for (const part of parts) {
    if (typeof cur !== "object" || cur === null || !(part in cur)) return undefined;
    cur = (cur as Record<string, unknown>)[part];
  }
  return typeof cur === "string" ? cur : undefined;
}

export function t(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const raw = resolvePath(messages[locale], key) ?? resolvePath(messages.ru, key) ?? key;
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? `{${name}}`));
}

const ERROR_KEY_MAP: Record<string, string> = {
  "Введите номер +998": "errors.invalidPhone",
  "Сначала введите телефон": "errors.enterPhoneFirst",
  "Неверный код": "errors.invalidCode",
  "Не авторизован": "errors.notAuthorized",
  "Дроп ещё не начался": "errors.preDrop",
  "Заказ не найден": "errors.orderNotFound",
  "Не удалось войти": "waitlist.loginFailed",
};

export function translateError(locale: Locale, message: string): string {
  const key = ERROR_KEY_MAP[message];
  return key ? t(locale, key) : message;
}

export function localeToBcp47(locale: Locale): string {
  if (locale === "uz") return "uz-UZ";
  if (locale === "en") return "en-US";
  return "ru-RU";
}
